package escapesequences;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \n26");
        System.out.println("Java \t26");
        System.out.println("Java 26\b");
        System.out.println("I\'m a boy.");
        System.out.println("I\\m a boy.");
    }
}